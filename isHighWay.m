function [flag,originalWayID]=isHighWay(wayID)
% HighWayID=[123741422,123741446,123741454,123741462,123867420,123867421,123867422,133781767,133781807,...
% 222603227,222603229,22277701,26456098,27167766,27183379,27614857,28715669,29394019,29394023,31129732,31487921,...
% 31487930,33111467,41380022,43101607,43759512,48037052,48101169,48101177,48191417,48582237,5071582,58841187,8915137,8915249,...
% 8915288,8915478,8915500,8921980];
load('HighWayIDs.mat','mapHighWayID','HighWayID');
flag=0;
originalWayID=-1;
eps=1e-5;
for i=1:length(mapHighWayID)
    if abs(wayID-mapHighWayID(i))<eps
       flag=1; 
       originalWayID=HighWayID(i);
       break;
    end
end

end